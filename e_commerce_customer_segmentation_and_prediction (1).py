import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import datetime as dt
from sklearn.preprocessing import StandardScaler
from sklearn.cluster import KMeans, AgglomerativeClustering, DBSCAN
from sklearn.metrics import silhouette_score
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, confusion_matrix
from sklearn.model_selection import train_test_split
import pickle

"""Exploratory Data Analysis"""

data = pd.read_csv('/content/data (2).csv', encoding='latin1')

data.head()

data.info()

data.isnull().sum()

data.describe()

data.drop(['CustomerID'], axis=1)

data = data[(data['Quantity'] > 0) & (data['UnitPrice'] > 0)]

"""Feature Engineering"""

data['TotalSales'] = data['Quantity'] * data['UnitPrice']

data['InvoiceDate'] = pd.to_datetime(data['InvoiceDate'])

plt.figure(figsize=(12, 5))
plt.subplot(1, 2, 1)
sns.histplot(data.groupby('CustomerID')['TotalSales'].sum(), bins=50, kde=True)
plt.title('Customer Spending Pattern (Monetary)')
plt.xlim(0, 5000)

plt.figure(figsize=(12, 5))
plt.subplot(1, 2, 2)
data['Month'] = data['InvoiceDate'].dt.month
sns.countplot(data=data, x='Month')
plt.title('Purchase Patterns by Month')
plt.show()

plt.figure(figsize=(10,5))
data.groupby('Country')['TotalSales'].sum().sort_values(ascending=True).head(5).plot(kind='bar', color='teal')
plt.title('Top 5 Countries by Revenue')
plt.ylabel('Revenue')
plt.show()

data['InvoiceDate'] = pd.to_datetime(data['InvoiceDate'])
data['MonthYear'] = data['InvoiceDate'].dt.to_period('M')
data.groupby('MonthYear')['TotalSales'].sum().plot(kind='line', figsize=(12,5), marker='o')
plt.title('Monthly Revenue Trend')
plt.grid()
plt.show()

data_date = data['InvoiceDate'].max() + dt.timedelta(days=1)

rfm = data.groupby('CustomerID').agg({
    'InvoiceDate': lambda x: (data_date - x.max()).days, # Recency
    'InvoiceNo': 'nunique',                                 # Frequency
    'TotalSales': 'sum'                                    # Monetary
})

rfm.rename(columns={'InvoiceDate': 'Recency', 'InvoiceNo': 'Frequency', 'TotalSales': 'Monetary'}, inplace=True)

"""Data Preprocessing"""

rfm_log = np.log1p(rfm)
scaler = StandardScaler()
rfm_scaled = scaler.fit_transform(rfm_log)

"""Model Training"""

kmeans = KMeans(n_clusters=4, random_state=42, n_init=10)
km_labels = kmeans.fit_predict(rfm_scaled)
km_score = silhouette_score(rfm_scaled, km_labels)

plt.figure(figsize=(18, 6))
plt.subplot(1, 3, 1)
plt.scatter(rfm_scaled[:, 0], rfm_scaled[:, 2], c=km_labels, cmap='viridis', alpha=0.5)
plt.title(f'K-Means\nSilhouette: {km_score:.3f}')
plt.xlabel('Recency (Scaled)')
plt.ylabel('Monetary (Scaled)')
plt.tight_layout()
plt.show()

hier = AgglomerativeClustering(n_clusters=4)
hi_labels = hier.fit_predict(rfm_scaled)
hi_score = silhouette_score(rfm_scaled, hi_labels)

plt.figure(figsize=(18, 6))
plt.subplot(1, 3, 2)
plt.scatter(rfm_scaled[:, 0], rfm_scaled[:, 2], c=hi_labels, cmap='plasma', alpha=0.5)
plt.title(f'Hierarchical\nSilhouette: {hi_score:.3f}')
plt.xlabel('Recency (Scaled)')
plt.tight_layout()
plt.show()

dbscan = DBSCAN(eps=0.5, min_samples=5)
db_labels = dbscan.fit_predict(rfm_scaled)
db_score = silhouette_score(rfm_scaled, db_labels) if len(set(db_labels)) > 1 else 0

plt.figure(figsize=(18, 6))
plt.subplot(1, 3, 3)
plt.scatter(rfm_scaled[:, 0], rfm_scaled[:, 2], c=db_labels, cmap='coolwarm', alpha=0.5)
plt.title(f'DBSCAN\nSilhouette: {db_score:.3f}')
plt.xlabel('Recency (Scaled)')
plt.tight_layout()
plt.show()

"""Model Evaluation"""

print(f"K-Means Silhouette Score: {km_score:.4f}")
print(f"Hierarchical Silhouette Score: {hi_score:.4f}")
print(f"DBSCAN Silhouette Score: {db_score:.4f}")

"""Cluster Profiling"""

rfm['Cluster'] = km_labels

# Define customer segments based on cluster profiles
def name_segments(cluster):
    if cluster == 0: return 'Regulars'
    if cluster == 1: return 'At Risk'
    if cluster == 2: return 'Loyal Customers'
    return 'Champions (VIP)'

rfm['Segment'] = rfm['Cluster'].apply(name_segments)

# View the averages for each segment
profile = rfm.groupby('Segment').agg({
    'Recency': 'mean',
    'Frequency': 'mean',
    'Monetary': 'mean'
}).round(1)

print("Customer Segment Profiles:")
print(profile)

fig, axes = plt.subplots(1, 3, figsize=(18, 6))
colors = ['Blue', 'Red', 'Green', 'Yellow']

# 2. Plot each metric
