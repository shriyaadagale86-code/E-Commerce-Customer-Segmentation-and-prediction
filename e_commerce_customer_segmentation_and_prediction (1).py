for i, col in enumerate(metrics):
    profile[col].plot(kind='bar', ax=axes[i], color=colors, edgecolor='black')
    axes[i].set_title(f'Avg {col} by Segment', fontsize=14)
    axes[i].set_xlabel('')
    axes[i].tick_params(axis='x', rotation=45)

plt.tight_layout()
plt.show()

